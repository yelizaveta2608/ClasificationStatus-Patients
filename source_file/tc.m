if  kdtc<=0.10
    kkt='норма';
    osT=1;
elseif  kdtc<=0.25
    kkt='Відхилення від норми';
    osT=0.75
elseif kdtc<0.75
    kkt='Велике відхилення від норми';
    osT=0.25;
elseif 0.75<=kdtc
    kkt='Система в поганому стані';
    osT=0;
else
    kkt='помилка (перевірте введені дані)';
end
set(handles.edit4, 'String',['Коефіцієнт =' num2str(kdtc), ' ' kkt])


